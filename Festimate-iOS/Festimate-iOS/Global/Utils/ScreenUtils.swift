import UIKit

struct ScreenUtils {
    
    // MARK: 디바이스 width
    
    static var width: CGFloat {
        return UIScreen.main.bounds.width
    }
    
    
    // MARK: 디바이스 height
    
    static var height: CGFloat {
        return UIScreen.main.bounds.height
    }
    
}
